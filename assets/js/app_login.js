$(window).load(function() {   
   $('.uni_wrapper').removeClass('loadingDiv');
   
});
$(function() {  
	$('.forgot-con').hide();
    $('.forgot-pass').click(function() {
        $('.login-con').hide();
        $('.forgot-con').show();
    });
    $('.back-login').click(function() {
        $('.login-con').show();
        $('.forgot-con').hide();
    });
	$('.forgot-pass, .back-login').click(function(){
		$('.error-message').hide();
	});
});